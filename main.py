from pet import Pet

# Step 1: Create a pet named Buddy
my_pet = Pet("Buddy")

# Step 2: Show initial status
my_pet.get_status()

# Step 3: Feed the pet
my_pet.eat()

# Step 4: Let it sleep
my_pet.sleep()

# Step 5: Play with the pet
my_pet.play()

# Step 6: Teach it tricks
my_pet.train("sit")
my_pet.train("roll over")

# Step 7: Show the tricks
my_pet.show_tricks()

# Step 8: Final status check
my_pet.get_status()
